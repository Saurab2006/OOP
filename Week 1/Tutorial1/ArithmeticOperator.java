package Tutorial1;

public class ArithmeticOperator {
	public static void main(String[] args) {
		int a = 10 ;
		int b = 5;
		int sum = a + b;
		int diff = a - b;
		int product = a * b;
		int division = a / b;
		
		System.out.println("Sum: " + sum);
		System.out.println("Difference: " + diff);
		System.out.println("Product: " + product);
		System.out.println("Division: " + division);
	}

}
